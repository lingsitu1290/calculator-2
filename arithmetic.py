def add(num1, num2):
    """add num1 and num 2"""

    return num1 + num2


def subtract(num1, num2):
    """subtract num2 from num1"""

    return num1 - num2


def multiply(num1, num2):
    """ multiply num1 by num2"""

    return num1 * num2


def divide(num1, num2):
    """divide num1 by num2 

    num1 was turned into a float prior to division and answer is a float
    """

    return float(num1) / num2


def square(num1):
    """ multiply num1 by num1 to square num1"""

    return num1 ** 2


def cube(num1):
    """ raise num1 to the third power """

    return num1 ** 3


def power(num1, num2):
    """ raise num1 to the "num2" power"""

    return num1 ** num2


def mod(num1, num2):
    """ find the remainder of dividing num1 by num2 """

    return num1 % num2
